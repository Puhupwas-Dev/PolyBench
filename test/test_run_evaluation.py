def test_empty_model_patch(mock_instance, mock_docker_client, tmp_path):
    """Test behavior when model patch is empty"""
    # Docstring explaining the test case

    # Arrange: set the model_patch attribute to an empty string
    mock_instance.model_patch = ""
    # Convert temporary directory path to string for result_path
    result_path = str(tmp_path)

    # Act: call evaluate_instance with empty model patch
    evaluate_instance(
        instance=mock_instance,                # The mock instance with empty patch
        result_path=result_path,              # Directory to store results
        evaluate_gold=False,                  # Do not evaluate gold patch
        repo_path=str(tmp_path),              # Path to clone repositories
        delete_image=True,                    # Delete Docker image after run
        client=mock_docker_client,            # Mock Docker client
    )

    # Assert: verify that a result JSON file was created
    result_file = Path(result_path) / f"{mock_instance.instance_id}_result.json"
    assert result_file.exists()               # The result file should exist


